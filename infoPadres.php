						<p><br>La automatización de un proceso industrial, de maquinaria, o de diversos equipos industriales consiste en la incorporación al mismo de un conjunto de elementos y dispositivos tecnológicos que aseguren su control y buen comportamiento.</p>
						<p><br>Uno de los objetivos deseables es que el automatismo sea capaz de reaccionar frente a situaciones previstas de antemano y también que, frente a imponderables, tenga como objetivo situar al proceso y a los recursos humanos que lo asisten en la situación más favorable.</p>
						<p><br>Históricamente los objetivos de la automatización han sido el procurar reducción de costes de fabricación, una calidad constante en los medios de producción y liberar al ser humano de las tareas tediosas, peligrosas o insalubres, por ejemplo asignándole a robots este tipo de tareas.</p>
						<p><br>Sin embargo en tiempos recientes, debido a la alta competitividad empresarial y a la internacionalización creciente de los mercados, estos objetivos han sido ampliamente incrementados.</p>
						<p><br>Como consecuencia de un entorno competitivo, cualquier empresa se ve sometida a la necesidad de acometer a grandes y rápidos procesos de cambio en búsqueda de su adecuación a las demandas del mercado, neutralización de los avances de su competencia o simplemente como maniobra de cambio estratégica, al verse acortado el ciclo de vida de alguno de sus productos.</p>
						<p><br>Ello obliga a mantener medios de producción adecuados que posean una gran flexibilidad y puedan modificar oportunamente la estrategia de producción.</p>
						<p><br>Los desarrollos tecnológicos en la electrónica en cuanto a nuevos materiales y nuevos dispositivos ha tenido como consecuencia el que sean posibles mayores niveles de integración entre el sistema productivo y los centros de decisión y política empresarial, permitiendo que la producción pueda ser contemplada como un flujo de material a través del sistema productivo que interacciona con todas las áreas de la empresa.</p>
