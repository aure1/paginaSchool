							<h4><br><br><font color = "red">Normas de Operación de Titulación (Servicios Escolares)</h4></font>
							<p><br><b>Apertura de Expediente:</b> Será presentado la documentación correspondiente en el Área de Titulación, para programar el Protocolo.<br>
							<b>Acto de Titulación y/o Recepción Profesional:</b> Es el último requisito académico que debe cumplir el Pasante de Licenciatura ante un Comité de titulación para obtener su Título Profesional, 
							mediante un Protocolo.<br>
							<b>Protocolo:</b> Es la Ceremonia profesional en la cual el Pasante recibe la validación de su formación académica, se levanta Acta de Titulación (en donde se declara titulado), se toma Protesta de Ley, 
							(Juramento de Ética Profesional); por parte de la Institución, representada por el Comité de titulación debidamente constituido.<br>
							<p><b>Comité de Titulación (Sinodales):</b> Es quien coordina y dirige el acto protocolario para los Pasantes de Ingeniería, el cual es constituido por un Presidente, un Secretario y un Vocal, designados 
							por la Sub-Dirección Académica.  (Profesores del Tecnológico, deberán contar con Experiencia profesional, poseer Titulo y Cédula Profesional, tener conocimientos comprobables del tema presentado).<br>
							<p><b>Examen Profesional:</b> Es la presentación y réplica del trabajo profesional desarrollado por el Pasante para cualquiera de las opciones de titulación (con excepción de la opción IV Posgrado, 
							opción V Promedio General Sobresaliente, y opción XI Informe de Residencia,  en los que solo se lleva a cabo el protocolo) que será sometido a su acreditación por el Comité de Titulación.<br>
							<p><b>Título Profesional:</b> Es el documento expedido por la Institución, a favor del Pasante que haya concluido los requisitos académicos de titulación, el cual es necesario para la expedición de Cédula 
							Profesional.<br>
							</p>
							<h4><br><br><font color = "red">Opción de Titulación para el Plan 1993</h4></font>
							<p><br><b>Tesis Profesional:</b> Consiste en un trabajo escrito, detallado con calidad profesional, del nivel Licenciatura, con hipótesis, empastado, cuyo objetivo es la aportación de nuevos enfoques 
							sobre un tema relacionado con la carrera cursada, y requiere que el egresado presente examen profesional.<br></p>
							<p><br><b>Proyecto de Investigación:</b>  Consiste  en presentar un Informe Técnico,  acerca de la investigación realizada que conlleve a la propuesta, desarrollo o innovación del material, equipo, 
							prototipo, proceso o sistema; siendo sometido a examen profesional. (su asesor será el titular responsable 
							del proyecto de investigación). Este podrá ser iniciado a partir de que se tenga cubierto el 70% de créditos de la carrera.<br>
							Esta opción podrá ser presentada bajo las siguientes modalidades:<br>
							<b>• Individual: </b>Cuando lo desarrolla el alumno pasante de Licenciatura.<br>
							<b>• Colectiva: </b>Cuando lo desarrollan hasta cuatro alumnos pasantes.<br>
							<b>• Colectiva Interdisciplinaria: </b>Cuando lo desarrollen hasta seis alumnos pasantes y puede ser de diferentes carreras que se imparten en  el tecnológico. Acto continuo se sustenta el Protocolo.<br>
							</p>
							<p><br><b>Seminario de Titulación:</b> Consiste en que el egresado apruebe un seminario que para tal efecto determine el Consejo, con una duración mínima de 150 horas. Para acreditarlo deberá obtener una calificación  
							no menor a 80 y una asistencia mínima del 90%, además de presentar un Informe Técnico relacionado con el mismo.<br>
							</p>
							<p><br><b>Posgrado:</b> Consiste en cursar bajo las siguientes modalidades:<br>
							<b>•</b> Estudios afines a la carrera del egresado.<br>
							<b>•</b> Obtener diploma de especialización.<br>
							<b>•</b> Acreditar el 50% de maestría.<br>
							<b>•</b> Estudios diferentes a la carrera del egresado.<br>
							<b>•</b> Acreditar el 60%  de maestría.<br>
							</p>
							<p><br><b> Estas modalidades deberán ser aprobadas por el Consejo.</b></p>
							<p><br><b>Promedio General Sobresaliente:</b> 3.7 La escolaridad por promedio está dirigida al egresado que haya obtenido un promedio mínimo de 90 (noventa) o más durante toda su carrera, habiendo acreditado el 100% 
							(cien por ciento) de sus asignaturas en curso normal. Para el caso de egresados cuyo promedio aritmético general sea igual o mayor a noventa y ocho en el total de su carrera, habiendo acreditado el cien por ciento de 
							sus asignaturas en curso normal o primer examen global, tendrá derecho a que se le otorgue mención honorifica.<br></p>
							<p><br><b>Créditos Adicionales de Especialización:</b> Consiste en la acreditación por parte del alumno, de asignaturas de especialidad de su carrera u otra afín, el número de créditos estará determinado por el Consejo, con una duración 
							mínima de 150 horas. Para acreditarlo se deberá obtener una calificación de 90%, además de presentar un Informe Técnico relacionado con el mismo.<br></p>
							<p><br><b>Experiencia Profesional: </b> Consiste en la elaboración de un Informe Técnico, de la práctica profesional del egresado, durante un período no menor a un año efectivo en el área de conocimiento de su carrera. 
							El informe escrito deberá ser aprobado por el Consejo.<br></p>
							<p><br><b>Examen por Áreas del Conocimiento:</b> Consiste en la aprobación de un conjunto de exámenes sobre un grupo de áreas determinadas o de asignaturas de la carrera cursada. Este podrá aplicarse simultáneamente a todos aquellos 
							egresados que lo hayan solicitado. El examen de conocimientos deberá ser autorizado por el Consejo.<br></p>
							<p><br><b>Examen General de Egreso de Licenciatura (EGEL):</b> Lo aplica el Centro Nacional de Evaluación para la Educación Superior, A.C. (CENEVAL), consiste en obtener el testimonio de alto rendimiento o el de desempeño académico satisfactorio.<br></p>
							<p><br><b>Diseño o Rediseño de Equipo, Aparato o Máquina:</b> Se elaborará un Informe Técnico, del diseño o modificación de alguno de los componentes originales, que tiendan a mejorar su funcionamiento, con gran impacto económico y calidad útil 
							para la empresa o sociedad. El informe deberá ser aprobado por el Consejo.<br></p>
