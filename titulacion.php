							<h4><br><br><font color = "red">Características Generales</h4></font>
							<p><br>Para la presentación del <b>Informe Técnico</b> escrito:<br>
							<b>•</b> Entregar el trabajo escrito en computadora, en papel tamaño carta y a doble espacio (letra arial No. 12).<br>
							<b>•</b> Iniciar cada capítulo en hoja aparte.<br>
							<b>•</b> Realizar los dibujos, tablas, gráficas y diagramas necesarios de calidad profesional.<br>
							<b>•</b> Usar el sistema internacional de medidas. (margen Izq.  3 cm.  márgenes Superior e Inferior y derecho de 2.5<br>
							<b>•</b> Utilizar pie de página.<br>
							<b>•</b> Iniciar la paginación en el capítulo de introducción, para los capítulos anteriores utilizar números romanos en minúsculas.<br>
							<b>•</b> Utilizar el sistema de numeración decimal o el alfanumérico, sin combinarlos.<br>
							<b>•</b> En caso de utilizar citas textuales, anotar  con sangría y entre comillas.<br>
							<b>•</b> Hablar de manera impersonal y utilizar verbos en tiempo presente.<br>
							<b>•</b> Empastado (imprimiendo en la portada; Logo del ITS Zapopan,  la opción de Titulación, Nombre completo del alumno, y carrera).<br>
